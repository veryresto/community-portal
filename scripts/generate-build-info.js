import { execSync } from 'child_process';
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = join(__dirname, '..');

export function generate(mode = 'production') {
  const pkgPath = join(projectRoot, 'package.json');
  let version = '0.0.0';
  try {
    const pkg = JSON.parse(readFileSync(pkgPath, 'utf-8'));
    version = pkg.version || '0.0.0';
  } catch (err) {
    console.warn('[build-metadata] Could not read package.json version:', err.message);
  }

  let gitCommitSha = 'unknown';
  let gitBranch = 'unknown';

  // 1. Read from environment variables (useful in Docker/CI contexts)
  if (process.env.VITE_GIT_SHA) {
    gitCommitSha = process.env.VITE_GIT_SHA;
  }
  if (process.env.GIT_BRANCH || process.env.VITE_GIT_BRANCH) {
    gitBranch = process.env.GIT_BRANCH || process.env.VITE_GIT_BRANCH;
  }

  // 2. Fallback to local git execution if still unknown
  if (gitCommitSha === 'unknown' || gitBranch === 'unknown') {
    try {
      if (gitCommitSha === 'unknown') {
        gitCommitSha = execSync('git rev-parse HEAD').toString().trim();
      }
      if (gitBranch === 'unknown') {
        gitBranch = execSync('git rev-parse --abbrev-ref HEAD').toString().trim();
      }
    } catch (err) {
      // Git metadata not available
    }
  }

  const buildTimestamp = new Date().toISOString();

  const content = `// This file is auto-generated by the build metadata system. Do not edit manually.
export const buildInfo = {
  version: ${JSON.stringify(version)},
  gitCommitSha: ${JSON.stringify(gitCommitSha)} as string,
  gitBranch: ${JSON.stringify(gitBranch)} as string,
  buildTimestamp: ${JSON.stringify(buildTimestamp)},
  environment: ${JSON.stringify(mode)},
} as const;

export type BuildInfo = typeof buildInfo;
`;

  const outputPath = join(projectRoot, 'src', 'generated', 'build-info.ts');
  try {
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, content, 'utf-8');
    console.log(`[build-metadata] Generated build info at src/generated/build-info.ts (Env: ${mode}, Version: ${version})`);
  } catch (err) {
    console.error('[build-metadata] Failed to write build-info.ts:', err);
  }
}

// Run if called directly
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const mode = process.argv[2] || 'production';
  generate(mode);
}
